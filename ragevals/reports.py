"""Report generation for evaluation results."""

from __future__ import annotations

from datetime import datetime

import pandas as pd


def generate_markdown_report(
    results_df: pd.DataFrame,
    metric_cols: list[str],
    config: dict | None = None,
    experiment_results: pd.DataFrame | None = None,
) -> str:
    """Generate a comprehensive Markdown evaluation report.

    Args:
        results_df: DataFrame with metric scores per test case.
        metric_cols: Column names for metrics.
        config: Pipeline configuration dict.
        experiment_results: Optional DataFrame from hyperparameter experiments.

    Returns:
        Markdown report string.
    """
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    config = config or {}

    avg_scores = results_df[metric_cols].mean()
    failing = [m for m in metric_cols if avg_scores[m] < 0.7]

    report = f"""# RAG Evaluation Report

**Generated**: {timestamp}
**Test Cases**: {len(results_df)}
**Metrics**: {len(metric_cols)}

---

## Executive Summary

**Overall Quality**: {"GOOD" if not failing else "NEEDS IMPROVEMENT"}
- Metrics meeting threshold (>= 0.7): {len(metric_cols) - len(failing)}/{len(metric_cols)}
{"- Failing: " + ", ".join(failing) if failing else "- All metrics passing"}

## Metric Scores

| Metric | Mean | Min | Max | Std | Status |
|--------|------|-----|-----|-----|--------|
"""

    for m in metric_cols:
        mean = results_df[m].mean()
        mn = results_df[m].min()
        mx = results_df[m].max()
        std = results_df[m].std()
        status = "PASS" if mean >= 0.7 else "FAIL"
        report += f"| {m} | {mean:.3f} | {mn:.3f} | {mx:.3f} | {std:.3f} | {status} |\n"

    # Worst queries
    df_copy = results_df.copy()
    df_copy["_avg"] = df_copy[metric_cols].mean(axis=1)
    worst = df_copy.nsmallest(3, "_avg")

    report += "\n## Worst-Performing Queries\n\n| Query | Avg Score |\n|-------|-----------|\n"
    for _, row in worst.iterrows():
        q = str(row.get("query", ""))[:60]
        report += f"| {q} | {row['_avg']:.3f} |\n"

    if experiment_results is not None and not experiment_results.empty:
        best = experiment_results.iloc[0]
        report += f"\n## Hyperparameter Findings\n\n"
        report += f"Best config: chunk_size={best.get('chunk_size', 'N/A')}, "
        report += f"top_k={best.get('top_k', 'N/A')}, "
        report += f"temperature={best.get('temperature', 'N/A')}\n"

    report += "\n## Recommendations\n\n"
    if any("faithfulness" in m and avg_scores[m] < 0.8 for m in metric_cols):
        report += "- **Faithfulness**: Strengthen grounding instructions. Lower temperature.\n"
    if any("recall" in m and avg_scores[m] < 0.8 for m in metric_cols):
        report += "- **Context Recall**: Improve embeddings or increase top-K.\n"
    if any("relevancy" in m and avg_scores[m] < 0.8 for m in metric_cols):
        report += "- **Relevancy**: Refine prompt template for more focused answers.\n"
    if not failing:
        report += "- All metrics are healthy. Consider tightening thresholds.\n"

    report += f"\n---\n*Generated by ragevals v0.1.0*\n"
    return report


def generate_html_report(
    results_df: pd.DataFrame,
    metric_cols: list[str],
    config: dict | None = None,
    experiment_results: pd.DataFrame | None = None,
) -> str:
    """Generate an HTML evaluation report.

    Wraps the Markdown report in a styled HTML template.
    """
    md_content = generate_markdown_report(
        results_df, metric_cols, config, experiment_results
    )

    # Convert basic markdown to HTML
    html_body = md_content
    for line in md_content.split("\n"):
        if line.startswith("# "):
            html_body = html_body.replace(line, f"<h1>{line[2:]}</h1>")
        elif line.startswith("## "):
            html_body = html_body.replace(line, f"<h2>{line[3:]}</h2>")

    html_body = html_body.replace("\n", "<br>\n")

    return f"""<!DOCTYPE html>
<html>
<head>
    <meta charset="utf-8">
    <title>RAG Evaluation Report</title>
    <style>
        body {{ font-family: -apple-system, BlinkMacSystemFont, sans-serif; max-width: 900px; margin: 40px auto; padding: 0 20px; color: #333; }}
        h1 {{ color: #1a1a2e; border-bottom: 2px solid #16213e; padding-bottom: 10px; }}
        h2 {{ color: #16213e; margin-top: 30px; }}
        table {{ border-collapse: collapse; width: 100%; margin: 15px 0; }}
        th, td {{ border: 1px solid #ddd; padding: 8px 12px; text-align: left; }}
        th {{ background: #16213e; color: white; }}
        tr:nth-child(even) {{ background: #f9f9f9; }}
    </style>
</head>
<body>
{html_body}
</body>
</html>"""
